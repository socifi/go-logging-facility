package main

import (
	"encoding/json"
	"fmt"
	"github.com/socifi/golog/handler/init"
	_ "github.com/socifi/golog/hook/dsn"
	"io/ioutil"
	"os"
)

type autoGenerated struct {
	/* Your config here... */
	Logging log.Config `json:"logging"`
}

func main() {
	raw, err := ioutil.ReadFile("./config.json")
	if err != nil {
		fmt.Println(err.Error())
		os.Exit(1)
	}

	var c autoGenerated
	err = json.Unmarshal(raw, &c)
	if err != nil {
		fmt.Println("Error parsing json:", err.Error())
		return
	}
	//	fmt.Printf("%#v\n", c)
	logger, err := log.Init(c.Logging)
	if err != nil {
		fmt.Println("Error initializing logger:", err.Error())
		return
	}
	logger.WithField("dsn", "host=localhost port=5432 user=postgres password=postgres dbname=postgres sslmode=disable").WithField("a", "b").Info("trial")
	logger.WithField("dsn", "host=localhost port=5432 user=postgres password=postgres dbname=postgres sslmode=disable").WithField("a", "b").Warn("trial")
}
